#!/bin/python

a = eval(input('a: '))
b = float(input('b: '))
c = int(input('c: '))
d = bool(eval(input('d: ')))
e = input('e: ')

print('a: {} ({})'.format(a,type(a)))
print('b: {} ({})'.format(b,type(b)))
print('c: {} ({})'.format(c,type(c)))
print('d: {} ({})'.format(d,type(d)))
print('e: {} ({})'.format(e,type(e)))


